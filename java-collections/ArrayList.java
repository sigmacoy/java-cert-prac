// Adding Elements
// .add(element) - adds element at the end
// .add(index, element) - adds element at specific position
// .addAll(collection) - adds all elements from another collection
// .addAll(index, collection) - adds all elements at specific position

// Removing Elements
// .remove(index) - removes element at index, returns removed element
// .remove(element) - removes first occurrence, returns true/false
// .removeAll(collection) - removes all elements in collection
// .removeIf(predicate) - removes elements that match condition
// .clear() - removes all elements

// Accessing Elements
// .get(index) - returns element at index
// .set(index, element) - replaces element at index, returns old element
// .indexOf(element) - returns first occurrence index, -1 if not found
// .lastIndexOf(element) - returns last occurrence index, -1 if not found
// .contains(element) - returns true if element exists
// .containsAll(collection) - returns true if all elements exist

// Iterating
// .iterator() - returns iterator
// .listIterator() - returns list iterator
// .listIterator(index) - returns list iterator starting at index
// .forEach(action) - performs action on each element

// Information
// .size() - returns number of elements
// .isEmpty() - returns true if empty
// .subList(fromIndex, toIndex) - returns portion of list

// Conversion
// .toArray() - converts to Object[]
// .toArray(T[] a) - converts to specified type array

// Sorting & Processing
// .sort(comparator) - sorts using comparator
// .replaceAll(unaryOperator) - replaces each element with operation result
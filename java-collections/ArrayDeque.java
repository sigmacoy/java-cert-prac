// resizable array

// Adding Elements
// .add(element) - adds element at the end, returns true/false
// .addFirst(element) - adds at beginning
// .addLast(element) - adds at end
// .offer(element) - adds at end, returns true/false
// .offerFirst(element) - adds at beginning, returns true/false
// .offerLast(element) - adds at end, returns true/false
// .push(element) - adds at beginning (Stack method)

// Removing Elements
// .remove() - removes and returns first element
// .removeFirst() - removes and returns first element
// .removeLast() - removes and returns last element
// .removeFirstOccurrence(element) - removes first occurrence
// .removeLastOccurrence(element) - removes last occurrence
// .poll() - removes and returns first element, null if empty
// .pollFirst() - removes and returns first element, null if empty
// .pollLast() - removes and returns last element, null if empty
// .pop() - removes and returns first element (Stack method)
// .clear() - removes all elements

// Examining Elements
// .element() - returns first element, throws if empty
// .getFirst() - returns first element, throws if empty
// .getLast() - returns last element, throws if empty
// .peek() - returns first element, null if empty
// .peekFirst() - returns first element, null if empty
// .peekLast() - returns last element, null if empty

// Other Methods
// .contains(element) - returns true if element exists
// .size() - returns number of elements
// .isEmpty() - returns true if empty
// .iterator() - returns iterator from first to last
// .descendingIterator() - returns iterator from last to first
// .toArray() - converts to array
// .clone() - returns shallow copy
// LIFO - Legacy in Java

// Adding Elements
// .push(element) - adds element to top, returns element
// .add(element) - adds element (from Vector)
// .add(index, element) - adds at position (from Vector)

// Removing Elements
// .pop() - removes and returns top element, throws if empty
// .remove(index) - removes at index (from Vector)
// .remove(element) - removes element (from Vector)
// .clear() - removes all elements

// Examining Elements
// .peek() - returns top without removing, throws if empty
// .empty() - returns true if stack is empty
// .search(element) - returns 1-based position from top, -1 if not found
